const JSON_RESPONSE_FORMAT =
  '只输出 JSON：{"humanText":"直接结果","breakdown":[{"from":"原词","to":"真实意思"}]}'

const DIRECT_STYLE_RULES = [
  '直接下结论，不解释，不分析，不写铺垫。',
  '用最尖酸刻薄、阴阳怪气的语气，越毒舌越好，像一个看透一切的损友在骂醒你。',
  '可以用反讽、嘲笑、挖苦，把对方的虚伪体面话扒得一丝不挂。',
  '优先点破钱、强度、风险、关系走向、真实态度，往最扎心的地方戳。',
  '不要用"说白了""翻译一下""潜台词是"这种开头。不要重复原文。',
  '句子要短，最狠的往往只有一两句。多用"X个头""笑死""做梦呢""关你屁事"等网络嘲讽口语收尾。',
  'humanText 控制在 2-3 句，越短越辣，不要长篇大论。',
  'breakdown 取 1-6 个词或短句；from 保留原文，to 直接写真实意思，越扎心越好，不超过 20 个字。'
].join('\n')

function buildPrompt(role, task, focus, cues, examples) {
  const exampleText = examples
    .map((e, i) => `示例${i + 1}\n用户输入：${e.surface}\n你的输出：${e.truth}`)
    .join('\n')
  return [
    `角色：${role}`,
    `任务：${task}`,
    `重点：${focus}`,
    `抓词：${cues}`,
    DIRECT_STYLE_RULES,
    exampleText,
    JSON_RESPONSE_FORMAT
  ].join('\n')
}

