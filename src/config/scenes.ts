export type SceneId = 'work' | 'agent' | 'date' | 'client' | 'hr'

export interface TranslationBreakdown {
  from: string
  to: string
}

export interface TranslationResult {
  humanText: string
  breakdown: TranslationBreakdown[]
}

export interface SceneConfig {
  id: SceneId
  icon: string
  label: string
  placeholder: string
  systemPrompt: string
  temperature: number
}

const JSON_RESPONSE_FORMAT =
  '只返回 JSON，不要代码块和解释：{"humanText":"直接翻译结果","breakdown":[{"from":"原词","to":"真实意思"}]}'

const DIRECT_STYLE_RULES = `输出风格：
1. 不要解释，不要分析，不要短评。
2. 不要写成段子，不要文艺，不要拐弯。
3. 直接说结论，像朋友当面戳破。
4. 允许高级阴阳怪气、讽刺、毒舌，语气要狠，像把体面话当场撕开。
5. 尽量用短句，1-2 句解决。
6. 优先输出对当事人最直接的影响：裁员、压工资、甩锅、画饼、拒绝、拖延、压榨、敷衍。
7. 不要用“说白了、翻译一下、潜台词是”这种铺垫，直接进入结论。
8. 不要保留原话的体面感，要把包装层撕掉。
9. 如果一句话里有多个结论，拆成 2-4 个短分句，用逗号或换行隔开。
10. 最后半句可以补一刀，让嘲讽感更明显，但不要写成长篇。
11. 如果原话明显在敷衍、画饼、甩锅、拒绝，输出必须让人一眼看出冷漠和算计。
12. 下方示例的语气优先级最高；遇到相似场景，优先模仿示例里的阴阳怪气和补刀力度。`

const DIRECT_BREAKDOWN_RULES =
  'breakdown 提取 1-6 个最值得翻译的词或短句；from 保留原文，to 直接写真实意思，可以带刺，不要解释，不要超过 18 个字。'

function buildDirectPrompt(content: string) {
  return `${content}

${DIRECT_STYLE_RULES}

${DIRECT_BREAKDOWN_RULES}

${JSON_RESPONSE_FORMAT}`
}

