export type SceneId = 'work' | 'agent' | 'date' | 'client' | 'hr' | 'reverse'

export interface TranslationBreakdown {
  from: string
  to: string
}

export interface TranslationResult {
  humanText: string
  score: number
  verdict: string
  breakdown: TranslationBreakdown[]
}

export interface SceneConfig {
  id: SceneId
  icon: string
  label: string
  placeholder: string
  systemPrompt: string
}

const JSON_RESPONSE_FORMAT = `请严格按以下JSON格式返回，不要返回任何其他内容：
{
  "humanText": "完整翻译后的大白话版本",
  "score": 87,
  "verdict": "重度浓妆",
  "breakdown": [
    { "from": "原文中的术语/话术", "to": "对应的大白话翻译" }
  ]
}`

const SCORE_RULES = `【妆感评分】给出 0-100 的整数分数。
评分维度：
1. 信息密度：真正有用的信息占比
2. 术语浓度：黑话、行业术语、包装词的出现频率
3. 模糊程度：是否存在大量无法执行的模糊表达
4. 冗余程度：去掉包装后实际内容缩减了多少

【诊断标签】根据分数严格使用以下标签：
0-20：素颜真人
21-40：轻度滤镜
41-60：日常淡妆
61-80：中度美颜
81-95：重度浓妆
96-100：整容级别`

const BREAKDOWN_RULES = `【逐项拆解】找出原文里值得翻译的术语、话术或关键表达，逐条写进 breakdown。
要求：
- "from" 保留原文中的词或短句
- "to" 用简短有力的大白话解释
- 一句话里有多个黑话时可以拆成多条
- 如果原文本身很直白，breakdown 也至少给出 1 条最关键的解释`

function buildPrompt(content: string) {
  return `${content}

${SCORE_RULES}

${BREAKDOWN_RULES}

${JSON_RESPONSE_FORMAT}`
}

